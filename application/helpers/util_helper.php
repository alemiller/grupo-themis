}

function get_last_element($string) {
    $string_tmp = explode('/', $string);
    $result = $string_tmp[sizeof($string_tmp) - 1];
    return $result;
}



function get_data($position, $data) {
    $array = array();
    $values = 0;
    foreach ($data->content->rows as $key => $value) {
        if (empty($array)) {

            $array[$value[$position]] = 1;
        } else {

            $flag = 0;
            foreach ($array as $k => $val) {

                if ($value[$position] == $k) {
                    $array[$k] ++;
                    $flag = 1;
                    break;
                }
            }
            if ($flag == 0) {
                $array[$value[$position]] = 1;
            }
        }
    }
    return $array;
}

